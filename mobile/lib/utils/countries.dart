List<Country> get sortedCountries {
  final list = [...kCountries];
  list.sort((a, b) => a.name.compareTo(b.name));
  return list;
}

String? dialCodeFor(String? iso) {
  if (iso == null) return null;
  for (final c in kCountries) {
    if (c.code == iso.toUpperCase()) return c.dialCode;
  }
  return null;
}

String formatPhone(String? phone, String? country) {
  if (phone == null || phone.isEmpty) return '—';
  final dial = dialCodeFor(country);
  if (dial == null) return phone;
  final local = phone.replaceAll(RegExp(r'\D'), '').replaceFirst(RegExp(r'^0+'), '');
  return '+$dial $local';
}
